package com.solar.tech.bean;

public class airCompayMoney {
	private String airCompay;
	private int upMoney;
	private int downMoney;
	private int upTekNum;
	private int downTekNum;
	
	public String getAirCompay() {
		return airCompay;
	}
	public void setAirCompay(String airCompay) {
		this.airCompay = airCompay;
	}
	public int getUpMoney() {
		return upMoney;
	}
	public void setUpMoney(int upMoney) {
		this.upMoney = upMoney;
	}
	public int getDownMoney() {
		return downMoney;
	}
	public void setDownMoney(int downMoney) {
		this.downMoney = downMoney;
	}
	public int getUpTekNum() {
		return upTekNum;
	}
	public void setUpTekNum(int upTekNum) {
		this.upTekNum = upTekNum;
	}
	public int getDownTekNum() {
		return downTekNum;
	}
	public void setDownTekNum(int downTekNum) {
		this.downTekNum = downTekNum;
	}
	
}
